package io.herd.common.test.controller;

import io.herd.common.test.service.TestService;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequestMapping("v1/test-rest-controller")
public class TestRestController {

    private final TestService testService;

    public TestRestController(TestService testService) {
        this.testService = testService;
    }

    @GetMapping("/info-controller")
    public String infoController() {
        return "Test Rest Controller";
    }

    @GetMapping("/info-service")
    public String infoService() {
        return testService.info();
    }
}
